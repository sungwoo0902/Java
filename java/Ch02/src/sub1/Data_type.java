public class Data_type {

	public static void main(String[] args) {
		// 변수
		int a = 3;
		int b = 7;
		
		a = 5;
		
		System.out.println("a : " +a);
		System.out.println("b : " +b);
		
		String name = "홍길동";
		System.out.println(name+"님 반갑습니다.");
		
		// 상수
		final int NUM = 5;
		
		// 상수는 값이 고정
		System.out.println("NUM : " +NUM);
		
		

	}

}
